public class RightRohmbus {
    public static void main(String[] args) {
        for(int col = 0; col<5;col++){
            for(int row = 0; row <=col;row++){
                System.out.print(" ");
            }
            for(int row = 0; row <5;row++){
                System.out.print("*");
            }
            System.out.println();
        }
        
    }
}
